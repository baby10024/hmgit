package po;

public class Carts {
	
	private int id;
	private int gid;
	private int gcount;
	private double gsum;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getGid() {
		return gid;
	}
	public void setGid(int gid) {
		this.gid = gid;
	}
	public int getGcount() {
		return gcount;
	}
	public void setGcount(int gcount) {
		this.gcount = gcount;
	}
	public double getGsum() {
		return gsum;
	}
	public void setGsum(double gsum) {
		this.gsum = gsum;
	}
	
	
	

}
